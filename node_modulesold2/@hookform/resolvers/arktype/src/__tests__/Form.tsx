import { render, screen } from "@testing-library/react";
import user from "@testing-library/user-event";
import { type } from "arktype";
import React from "react";
import { useForm } from "react-hook-form";
import { arktypeResolver } from "..";

const schema = type({
  username: "string>1",
  password: "string>1",
});

type FormData = typeof schema.infer & { unusedProperty: string };

interface Props {
  onSubmit: (data: FormData) => void;
}

function TestComponent({ onSubmit }: Props) {
  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<FormData>({
    resolver: arktypeResolver(schema), // Useful to check TypeScript regressions
  });

  return (
    <form onSubmit={handleSubmit(onSubmit)} data-oid="0b9b55u">
      <input {...register("username")} data-oid="phj11h2" />
      {errors.username && (
        <span role="alert" data-oid="i_rbjk:">
          {errors.username.message}
        </span>
      )}

      <input {...register("password")} data-oid="l_u1wkt" />
      {errors.password && (
        <span role="alert" data-oid="_f25108">
          {errors.password.message}
        </span>
      )}

      <button type="submit" data-oid="on05-mn">
        submit
      </button>
    </form>
  );
}

