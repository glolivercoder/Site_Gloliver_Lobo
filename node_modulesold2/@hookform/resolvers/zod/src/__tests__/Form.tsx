import { render, screen } from "@testing-library/react";
import user from "@testing-library/user-event";
import React from "react";
import { useForm } from "react-hook-form";
import { z } from "zod";
import { zodResolver } from "..";

const schema = z.object({
  username: z.string().nonempty({ message: "username field is required" }),
  password: z.string().nonempty({ message: "password field is required" }),
});

type FormData = z.infer<typeof schema> & { unusedProperty: string };

interface Props {
  onSubmit: (data: FormData) => void;
}

function TestComponent({ onSubmit }: Props) {
  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<FormData>({
    resolver: zodResolver(schema), // Useful to check TypeScript regressions
  });

  return (
    <form onSubmit={handleSubmit(onSubmit)} data-oid="h_qa:iw">
      <input {...register("username")} data-oid="r:f_sax" />
      {errors.username && (
        <span role="alert" data-oid="njtayou">
          {errors.username.message}
        </span>
      )}

      <input {...register("password")} data-oid="qej-1pu" />
      {errors.password && (
        <span role="alert" data-oid="x._1-70">
          {errors.password.message}
        </span>
      )}

      <button type="submit" data-oid="r-mgn28">
        submit
      </button>
    </form>
  );
}

