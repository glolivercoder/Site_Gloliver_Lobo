import { render, screen } from "@testing-library/react";
import user from "@testing-library/user-event";
import { Validator } from "fluentvalidation-ts";
import React from "react";
import { SubmitHandler, useForm } from "react-hook-form";
import { fluentValidationResolver } from "../fluentvalidation-ts";

type FormData = {
  username: string;
  password: string;
};

class FormDataValidator extends Validator<FormData> {
  constructor() {
    super();

    this.ruleFor("username")
      .notEmpty()
      .withMessage("username is a required field");
    this.ruleFor("password")
      .notEmpty()
      .withMessage("password is a required field");
  }
}

interface Props {
  onSubmit: SubmitHandler<FormData>;
}

function TestComponent({ onSubmit }: Props) {
  const {
    register,
    formState: { errors },
    handleSubmit,
  } = useForm({
    resolver: fluentValidationResolver(new FormDataValidator()), // Useful to check TypeScript regressions
  });

  return (
    <form onSubmit={handleSubmit(onSubmit)} data-oid="d6r4vn1">
      <input {...register("username")} data-oid="pzkzibl" />
      {errors.username && (
        <span role="alert" data-oid="zd2jxfz">
          {errors.username.message}
        </span>
      )}

      <input {...register("password")} data-oid="260m:4i" />
      {errors.password && (
        <span role="alert" data-oid="n71uyrw">
          {errors.password.message}
        </span>
      )}

      <button type="submit" data-oid="tr.j0xj">
        submit
      </button>
    </form>
  );
}

