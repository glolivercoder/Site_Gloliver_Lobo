import { Static, Type } from "@sinclair/typebox";
import { render, screen } from "@testing-library/react";
import user from "@testing-library/user-event";
import React from "react";
import { useForm } from "react-hook-form";
import { typeboxResolver } from "..";

const schema = Type.Object({
  username: Type.String({ minLength: 1 }),
  password: Type.String({ minLength: 1 }),
});

type FormData = Static<typeof schema> & { unusedProperty: string };

interface Props {
  onSubmit: (data: FormData) => void;
}

function TestComponent({ onSubmit }: Props) {
  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<FormData>({
    resolver: typeboxResolver(schema), // Useful to check TypeScript regressions
  });

  return (
    <form onSubmit={handleSubmit(onSubmit)} data-oid="u-j74lt">
      <input {...register("username")} data-oid="qyek6eg" />
      {errors.username && (
        <span role="alert" data-oid="wq:0i0c">
          {errors.username.message}
        </span>
      )}

      <input {...register("password")} data-oid="b.yn.2o" />
      {errors.password && (
        <span role="alert" data-oid="a9t0a47">
          {errors.password.message}
        </span>
      )}

      <button type="submit" data-oid="nmc5r2j">
        submit
      </button>
    </form>
  );
}

