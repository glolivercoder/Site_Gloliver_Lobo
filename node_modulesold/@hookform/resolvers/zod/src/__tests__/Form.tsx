import { render, screen } from "@testing-library/react";
import user from "@testing-library/user-event";
import React from "react";
import { useForm } from "react-hook-form";
import { z } from "zod";
import { zodResolver } from "..";

const schema = z.object({
  username: z.string().nonempty({ message: "username field is required" }),
  password: z.string().nonempty({ message: "password field is required" }),
});

type FormData = z.infer<typeof schema> & { unusedProperty: string };

interface Props {
  onSubmit: (data: FormData) => void;
}

function TestComponent({ onSubmit }: Props) {
  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<FormData>({
    resolver: zodResolver(schema), // Useful to check TypeScript regressions
  });

  return (
    <form onSubmit={handleSubmit(onSubmit)} data-oid="4nzu9fi">
      <input {...register("username")} data-oid="8u7kbbu" />
      {errors.username && (
        <span role="alert" data-oid="85h9d0u">
          {errors.username.message}
        </span>
      )}

      <input {...register("password")} data-oid="v560:wz" />
      {errors.password && (
        <span role="alert" data-oid="crqzo9q">
          {errors.password.message}
        </span>
      )}

      <button type="submit" data-oid="rtmnaoh">
        submit
      </button>
    </form>
  );
}

