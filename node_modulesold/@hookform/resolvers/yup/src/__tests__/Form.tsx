import { render, screen } from "@testing-library/react";
import user from "@testing-library/user-event";
import React from "react";
import { SubmitHandler, useForm } from "react-hook-form";
import * as Yup from "yup";
import { yupResolver } from "..";

const schema = Yup.object({
  username: Yup.string().required(),
  password: Yup.string().required(),
});

type FormData = Yup.InferType<typeof schema>;

interface Props {
  onSubmit: SubmitHandler<FormData>;
}

function TestComponent({ onSubmit }: Props) {
  const {
    register,
    formState: { errors },
    handleSubmit,
  } = useForm({
    resolver: yupResolver(schema), // Useful to check TypeScript regressions
  });

  return (
    <form onSubmit={handleSubmit(onSubmit)} data-oid="jdds_lh">
      <input {...register("username")} data-oid="likqx:1" />
      {errors.username && (
        <span role="alert" data-oid="5gsjzf9">
          {errors.username.message}
        </span>
      )}

      <input {...register("password")} data-oid="88xt_qr" />
      {errors.password && (
        <span role="alert" data-oid=":wmd_7u">
          {errors.password.message}
        </span>
      )}

      <button type="submit" data-oid="7j1eww3">
        submit
      </button>
    </form>
  );
}

