import { render, screen } from "@testing-library/react";
import user from "@testing-library/user-event";
import { JSONSchemaType } from "ajv";
import React from "react";
import { useForm } from "react-hook-form";
import { ajvResolver } from "..";

type FormData = { username: string; password: string };

const schema: JSONSchemaType<FormData> = {
  type: "object",
  properties: {
    username: {
      type: "string",
      minLength: 1,
      errorMessage: { minLength: "username field is required" },
    },
    password: {
      type: "string",
      minLength: 1,
      errorMessage: { minLength: "password field is required" },
    },
  },
  required: ["username", "password"],
  additionalProperties: false,
};

interface Props {
  onSubmit: (data: FormData) => void;
}

function TestComponent({ onSubmit }: Props) {
  const {
    register,
    formState: { errors },
    handleSubmit,
  } = useForm<FormData>({
    resolver: ajvResolver(schema), // Useful to check TypeScript regressions
  });

  return (
    <form onSubmit={handleSubmit(onSubmit)} data-oid=":n:0_-f">
      <input {...register("username")} data-oid="e3-rr7-" />
      {errors.username && (
        <span role="alert" data-oid="3120x70">
          {errors.username.message}
        </span>
      )}

      <input {...register("password")} data-oid="a3w5a0r" />
      {errors.password && (
        <span role="alert" data-oid="kdpiqxs">
          {errors.password.message}
        </span>
      )}

      <button type="submit" data-oid="5076u:3">
        submit
      </button>
    </form>
  );
}

test("form's validation with Ajv and TypeScript's integration", async () => {
  const handleSubmit = vi.fn();
  render(<TestComponent onSubmit={handleSubmit} data-oid="ri42dbt" />);

  expect(screen.queryAllByRole("alert")).toHaveLength(0);

  await user.click(screen.getByText(/submit/i));

  expect(screen.getByText(/username field is required/i)).toBeInTheDocument();
  expect(screen.getByText(/password field is required/i)).toBeInTheDocument();
  expect(handleSubmit).not.toHaveBeenCalled();
});
