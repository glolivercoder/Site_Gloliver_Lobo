import { Static, Type } from "@sinclair/typebox";
import { TypeCompiler } from "@sinclair/typebox/compiler";
import { render, screen } from "@testing-library/react";
import user from "@testing-library/user-event";
import React from "react";
import { useForm } from "react-hook-form";
import { typeboxResolver } from "..";

const schema = Type.Object({
  username: Type.String({ minLength: 1 }),
  password: Type.String({ minLength: 1 }),
});

const typecheck = TypeCompiler.Compile(schema);

type FormData = Static<typeof schema> & { unusedProperty: string };

interface Props {
  onSubmit: (data: FormData) => void;
}

function TestComponent({ onSubmit }: Props) {
  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<FormData>({
    resolver: typeboxResolver(typecheck), // Useful to check TypeScript regressions
  });

  return (
    <form onSubmit={handleSubmit(onSubmit)} data-oid="6xq_-j5">
      <input {...register("username")} data-oid="i3-da9d" />
      {errors.username && (
        <span role="alert" data-oid="xzvk2b1">
          {errors.username.message}
        </span>
      )}

      <input {...register("password")} data-oid="bu:t6c4" />
      {errors.password && (
        <span role="alert" data-oid="x3.g.zj">
          {errors.password.message}
        </span>
      )}

      <button type="submit" data-oid="keinfan">
        submit
      </button>
    </form>
  );
}

